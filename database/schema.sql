    INDEX `idx_model` (`model`),
    INDEX `idx_acquisition_type` (`acquisition_type`),
    INDEX `idx_category` (`category`),
    INDEX `idx_status` (`status`),
    INDEX `idx_staff_id` (`staff_id`),
    INDEX `idx_processor` (`processor`),
    INDEX `idx_memory` (`memory`),
    INDEX `idx_os` (`os`),
    INDEX `idx_storage` (`storage`),
    INDEX `idx_gpu` (`gpu`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Asset Trails Table
CREATE TABLE IF NOT EXISTS `asset_trails` (
    `trail_id` INT(11) NOT NULL AUTO_INCREMENT,
    `asset_type` ENUM('laptop_desktop', 'av', 'network') NOT NULL,
    `asset_id` INT(11) NOT NULL,
    `action_type` ENUM('CREATE', 'UPDATE', 'DELETE', 'STATUS_CHANGE', 'ASSIGNMENT_CHANGE', 'LOCATION_CHANGE') NOT NULL,
    `changed_by` INT(11) DEFAULT NULL,
    `tech_id` VARCHAR(100) DEFAULT NULL,
    `field_name` VARCHAR(100) DEFAULT NULL,
    `old_value` TEXT DEFAULT NULL,
    `new_value` TEXT DEFAULT NULL,
    `description` TEXT DEFAULT NULL,
    `ip_address` VARCHAR(45) DEFAULT NULL,
    `user_agent` TEXT DEFAULT NULL,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (`trail_id`),
    INDEX `idx_asset_type` (`asset_type`),
    INDEX `idx_asset_id` (`asset_id`),
    INDEX `idx_action_type` (`action_type`),
    INDEX `idx_changed_by` (`changed_by`),
    INDEX `idx_tech_id` (`tech_id`),
    INDEX `idx_created_at` (`created_at`),
    INDEX `idx_asset_composite` (`asset_type`, `asset_id`),
    FOREIGN KEY (`changed_by`) REFERENCES `technician`(`id`) ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Handover Table
CREATE TABLE IF NOT EXISTS `handover` (
    `handover_id` INT(11) NOT NULL AUTO_INCREMENT,
    `staff_id` INT(11) NOT NULL,
    `asset_type` ENUM('laptop_desktop', 'av', 'network') NOT NULL,
    `asset_id` INT(11) NOT NULL,
    `handover_date` DATE NOT NULL,
    `handover_location` VARCHAR(255) DEFAULT NULL,
    `condition_agreement` BOOLEAN DEFAULT FALSE,
    `handover_notes` TEXT DEFAULT NULL,
    `digital_signoff` VARCHAR(100) DEFAULT NULL,
    `status` ENUM('active', 'returned', 'completed') DEFAULT 'active',
    `return_date` DATE DEFAULT NULL,
    `returned_by` INT(11) DEFAULT NULL,
    `return_condition` VARCHAR(50) DEFAULT NULL,
    `return_notes` TEXT DEFAULT NULL,
    `received_by_name` VARCHAR(100) DEFAULT NULL,
    `received_by_designation` VARCHAR(100) DEFAULT NULL,
    `desktop_condition` VARCHAR(20) DEFAULT NULL,
    `desktop_status` VARCHAR(20) DEFAULT NULL,
    `harddisk_condition` VARCHAR(20) DEFAULT NULL,
    `harddisk_status` VARCHAR(20) DEFAULT NULL,
    `monitor_condition` VARCHAR(20) DEFAULT NULL,
    `monitor_status` VARCHAR(20) DEFAULT NULL,
    `mouse_condition` VARCHAR(20) DEFAULT NULL,
    `mouse_status` VARCHAR(20) DEFAULT NULL,
    `keyboard_condition` VARCHAR(20) DEFAULT NULL,
    `keyboard_status` VARCHAR(20) DEFAULT NULL,
    `created_by` INT(11) DEFAULT NULL,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`handover_id`),
    INDEX `idx_staff_id` (`staff_id`),
    INDEX `idx_asset_type` (`asset_type`),
    INDEX `idx_asset_id` (`asset_id`),
    INDEX `idx_status` (`status`),
    INDEX `idx_handover_date` (`handover_date`),
    INDEX `idx_created_by` (`created_by`),
    INDEX `idx_returned_by` (`returned_by`),
    INDEX `idx_asset_composite` (`asset_type`, `asset_id`),
    FOREIGN KEY (`staff_id`) REFERENCES `staff_list`(`staff_id`) ON DELETE RESTRICT ON UPDATE CASCADE,
    FOREIGN KEY (`created_by`) REFERENCES `technician`(`id`) ON DELETE SET NULL ON UPDATE CASCADE,
    FOREIGN KEY (`returned_by`) REFERENCES `technician`(`id`) ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Repair / Faulty Intake Table
CREATE TABLE IF NOT EXISTS `repair_faulty` (
    `repair_id` INT(11) NOT NULL AUTO_INCREMENT,
    `asset_type` ENUM('laptop_desktop', 'av', 'network') NOT NULL,
    `asset_id` INT(11) NOT NULL,
    `reported_by` INT(11) DEFAULT NULL,
    `reported_by_name` VARCHAR(100) DEFAULT NULL,
    `severity` ENUM('Low', 'Medium', 'High', 'Critical') DEFAULT NULL,
    `issue_description` TEXT NOT NULL,
    `actions_performed` TEXT DEFAULT NULL,
    `parts_used` VARCHAR(255) DEFAULT NULL,
    `estimated_cost` DECIMAL(10,2) DEFAULT NULL,
    `vendor` VARCHAR(255) DEFAULT NULL,
    `return_actual_date` DATE DEFAULT NULL,
    `remarks` TEXT DEFAULT NULL,
    `created_by` INT(11) DEFAULT NULL,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `updated_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (`repair_id`),
    INDEX `idx_asset_type` (`asset_type`),
    INDEX `idx_asset_id` (`asset_id`),
    INDEX `idx_reported_by` (`reported_by`),
    INDEX `idx_severity` (`severity`),
    INDEX `idx_return_actual_date` (`return_actual_date`),
    INDEX `idx_created_by` (`created_by`),
    INDEX `idx_created_at` (`created_at`),
    INDEX `idx_asset_composite` (`asset_type`, `asset_id`),
    FOREIGN KEY (`reported_by`) REFERENCES `technician`(`id`) ON DELETE SET NULL ON UPDATE CASCADE,
    FOREIGN KEY (`created_by`) REFERENCES `technician`(`id`) ON DELETE SET NULL ON UPDATE CASCADE
