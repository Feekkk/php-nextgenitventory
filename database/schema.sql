-- Laptop/Desktop Assets Table
CREATE TABLE IF NOT EXISTS `laptop_desktop_assets` (
    `asset_id` INT(11) NOT NULL AUTO_INCREMENT,
    `serial_num` VARCHAR(100) DEFAULT NULL,
    `brand` VARCHAR(100) DEFAULT NULL,
    `model` VARCHAR(100) DEFAULT NULL,
    `acquisition_type` VARCHAR(100) DEFAULT NULL,
    `category` VARCHAR(100) DEFAULT NULL,
    `status` VARCHAR(50) DEFAULT NULL,
    `staff_id` INT(11) DEFAULT NULL,
    `assignment_type` VARCHAR(100) DEFAULT NULL,
    `location` VARCHAR(100) DEFAULT NULL,
    `processor` VARCHAR(100) DEFAULT NULL,
    `memory` VARCHAR(100) DEFAULT NULL,
    `os` VARCHAR(100) DEFAULT NULL,
    `storage` VARCHAR(100) DEFAULT NULL,
    `gpu` VARCHAR(100) DEFAULT NULL,
    `warranty_expiry` DATE DEFAULT NULL,
    `part_number` VARCHAR(100) DEFAULT NULL,
    `supplier` VARCHAR(100) DEFAULT NULL,
    `period` VARCHAR(100) DEFAULT NULL,
